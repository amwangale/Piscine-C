#include <unistd.h>

int	main(void)
{
	char z;
	char Y;
	char retur;

	z = 'z';
	Y = 'Y';
	while(z >= 'b' && Y >= 'A')
	{
		write(1, &z, 1);
		write(1, &Y, 1);
		z -= 2;
		Y -= 2;
	}
	retur = '\n';
	write(1, &retur, 1);
	return (0);
}
