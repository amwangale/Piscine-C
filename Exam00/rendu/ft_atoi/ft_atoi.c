int	ft_atoi(const char *str)
{
	int n;
	int sign;

	while (*str)
	{
		if (*str == ' ')
			str++;
		else
			break;
	}
	sign = 1;
	if (*str == '-') {
		sign = -1;
		str++;
	}
	n = 0;
	while (*str)
	{
		n = n * 10 + *str - '0';
		str++;
	}
	n *= sign;
	return n;
}
