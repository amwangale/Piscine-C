int	ft_atoi(char *str)
{
	int ret;
	int sign;

	sign = 1;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	ret = 0;
	while (*str != '\0' && (*str >= '0' && *str <= '9'))
	{
		ret = ret * 10 + *str - '0';
		str++;
	}
	return (ret * sign);
}
